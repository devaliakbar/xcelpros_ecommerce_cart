  static double get fontSizeBXSS {
    return SizeConfig.height(1.6);
  }

  static double get fontSizeS {
    return SizeConfig.height(1.8);
  }

  //BETWEEN S AND M
  static double get fontSizeBSM {
    return SizeConfig.height(2);
  }

  static double get fontSizeM {
    return SizeConfig.height(2.2);
  }

  //BETWEEN M AND L
  static double get fontSizeBML {
    return SizeConfig.height(2.4);
  }

  static double get fontSizeL {
    return SizeConfig.height(2.6);
  }

  static double get fontSizeXL {
    return SizeConfig.height(3);
  }

  static double get fontSizeXXL {
    return SizeConfig.height(3.4);
  }
}
